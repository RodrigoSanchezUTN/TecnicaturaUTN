# Operador de comparacion
d = 4
b = 2
resultado = d == b 
print (resultado)

# Operador de diferencia
resultado = d != b 
print (resultado)

# Operador de Mayor que
resultado = d > b 
print (resultado)

# Operador de Menor que
resultado = d < b 
print (resultado)

# Operador de Menor o igual que
resultado = d <= b 
print (resultado)

# Operador de Mayor o igual que
resultado = d >= b 
print (resultado)


